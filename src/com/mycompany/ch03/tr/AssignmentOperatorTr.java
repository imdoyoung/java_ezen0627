package com.mycompany.ch03.tr;

public class AssignmentOperatorTr {

	public static void main(String[] args) {

		// 대입 연산자
		
		int result1 = 1;
		int result2 = 2;
		int result3 = 3;
		
		result1 += 10;	// 11
		result2 -= 10;	// -8
		result3 *= 10;	// 30
		
		System.out.println("result1 : " + result1);
		System.out.println("result2 : " + result2);
		System.out.println("result3 : " + result3);
		
	}

}
