package com.mycompany.ch03;

public class AssignmentOperatorEx6 {

	public static void main(String[] args) {

//		대입 연산자 (+=, -=, *=, ...)
		
		int result1 = 1;
		int result2 = 2;
		int result3 = 3;
 		
		result1 += 10;	// 11
//		result1 = result + 10
		System.out.println("result1  :" + result1);
		
		result2 -= 10;	// 8
//		result2 = result - 10;
		System.out.println("result2 : " + result2);
		
		result3 *= 10;	// 30
//		result4 = result * 10
		System.out.println("result3 : " + result3);
		
		
	}

}
